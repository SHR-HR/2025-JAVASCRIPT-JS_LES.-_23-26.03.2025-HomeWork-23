    // –ù–∞–ø—Ä–∏–º–µ—Ä: —É üçé –∑–≤—É–∫ –ø–æ–µ–¥–∞–Ω–∏—è, —É üí£ ‚Äî –≤–∑—Ä—ã–≤, —É ‚ö° ‚Äî –∑–≤—É–∫ –±–∞—Ñ—Ñ–∞ –∏ —Ç.–¥.
    playSound(foodTypes[food.type].sound);
