  // –£–¥–∞–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π —Å–µ–≥–º–µ–Ω—Ç –∑–º–µ–∏ (—Ö–≤–æ—Å—Ç), —á—Ç–æ–±—ã –æ–Ω–∞ –¥–≤–∏–≥–∞–ª–∞—Å—å –≤–ø–µ—Ä—ë–¥, –Ω–µ —É–¥–ª–∏–Ω—è—è—Å—å
  // –ò–Ω–∞—á–µ –æ–Ω–∞ –±—ã –ø—Ä–æ—Å—Ç–æ —Ä–æ—Å–ª–∞ –∫–∞–∂–¥—É—é –∏—Ç–µ—Ä–∞—Ü–∏—é ‚Äî –∞ —ç—Ç–æ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç —Ç–æ–ª—å–∫–æ –ø—Ä–∏ –ø–æ–µ–¥–∞–Ω–∏–∏ –µ–¥—ã
