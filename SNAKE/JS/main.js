    tries++; // —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫ –ø–æ–ø—ã—Ç–æ–∫

    // –ï—Å–ª–∏ –ø–æ–ø—ã—Ç–æ–∫ –±–æ–ª—å—à–µ 100 ‚Äî –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞ (–∞–≤–∞—Ä–∏–π–Ω–∞—è –∑–∞—â–∏—Ç–∞)
    if (tries > 100) break;

