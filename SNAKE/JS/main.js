// === –ó–∞–≥—Ä—É–∑–∫–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–æ–≥–æ —Ñ–æ–Ω–∞ —Å –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ–º —Ä–∞–∑–º–µ—Ä–∞ ===

// –§—É–Ω–∫—Ü–∏—è —Å–∂–∏–º–∞—é—â–∞—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –ø–µ—Ä–µ–¥ –µ–≥–æ —É—Å—Ç–∞–Ω–æ–≤–∫–æ–π –≤ –∫–∞—á–µ—Å—Ç–≤–µ —Ñ–æ–Ω–∞
// –ü–∞—Ä–∞–º–µ—Ç—Ä—ã:
// file - –∑–∞–≥—Ä—É–∂–µ–Ω–Ω—ã–π —Ñ–∞–π–ª –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
// callback - —Ñ—É–Ω–∫—Ü–∏—è, –∫–æ—Ç–æ—Ä–∞—è –±—É–¥–µ—Ç –≤—ã–∑–≤–∞–Ω–∞ —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º (—Å–∂–∞—Ç–æ–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –≤ —Ñ–æ—Ä–º–∞—Ç–µ base64)
function compressImage(file, callback) {

  // 1. –°–æ–∑–¥–∞–µ–º –æ–±—ä–µ–∫—Ç FileReader –¥–ª—è —á—Ç–µ–Ω–∏—è —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ —Ñ–∞–π–ª–∞
  const reader = new FileReader();

  // 2. –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –¥–ª—è —Å–æ–±—ã—Ç–∏—è, –∫–æ—Ç–æ—Ä–æ–µ –ø—Ä–æ–∏–∑–æ–π–¥–µ—Ç –ø—Ä–∏ —É—Å–ø–µ—à–Ω–æ–º —á—Ç–µ–Ω–∏–∏ —Ñ–∞–π–ª–∞
  reader.onload = function (event) {

    // 3. –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π –æ–±—ä–µ–∫—Ç Image, —á—Ç–æ–±—ã –∑–∞–≥—Ä—É–∑–∏—Ç—å –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∏ —Ä–∞–±–æ—Ç–∞—Ç—å —Å –Ω–∏–º
    const img = new Image();

    // 4. –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –¥–ª—è —Å–æ–±—ã—Ç–∏—è –∑–∞–≥—Ä—É–∑–∫–∏ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
    img.onload = function () {

      // 5. –°–æ–∑–¥–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç canvas (HTML5 –∫–∞–Ω–≤–∞—Å), —á—Ç–æ–±—ã —Ä–∏—Å–æ–≤–∞—Ç—å –Ω–∞ –Ω–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ
      const canvas = document.createElement("canvas");

      // 6. –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π —Ä–∞–∑–º–µ—Ä –¥–ª—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è (—à–∏—Ä–∏–Ω–∞ –∏ –≤—ã—Å–æ—Ç–∞)
      const maxSize = 800;  // –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è —à–∏—Ä–∏–Ω–∞/–≤—ã—Å–æ—Ç–∞ –¥–ª—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è (800px)

      let w = img.width, h = img.height;  // –ò–∑–≤–ª–µ–∫–∞–µ–º —à–∏—Ä–∏–Ω—É –∏ –≤—ã—Å–æ—Ç—É –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è

      // 7. –ï—Å–ª–∏ —à–∏—Ä–∏–Ω–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –±–æ–ª—å—à–µ –≤—ã—Å–æ—Ç—ã –∏ –æ–Ω–∞ –±–æ–ª—å—à–µ maxSize, —É–º–µ–Ω—å—à–∞–µ–º —à–∏—Ä–∏–Ω—É –¥–æ maxSize, –ø—Ä–æ–ø–æ—Ä—Ü–∏–æ–Ω–∞–ª—å–Ω–æ —É–º–µ–Ω—å—à–∞–µ–º –≤—ã—Å–æ—Ç—É
      if (w > h && w > maxSize) {
        h *= maxSize / w; // –ü—Ä–æ–ø–æ—Ä—Ü–∏–æ–Ω–∞–ª—å–Ω–æ —É–º–µ–Ω—å—à–∞–µ–º –≤—ã—Å–æ—Ç—É
        w = maxSize;      // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é —à–∏—Ä–∏–Ω—É
      }
      // 8. –ï—Å–ª–∏ –≤—ã—Å–æ—Ç–∞ –±–æ–ª—å—à–µ maxSize, —É–º–µ–Ω—å—à–∞–µ–º –≤—ã—Å–æ—Ç—É –¥–æ maxSize, –ø—Ä–æ–ø–æ—Ä—Ü–∏–æ–Ω–∞–ª—å–Ω–æ —É–º–µ–Ω—å—à–∞–µ–º —à–∏—Ä–∏–Ω—É
      else if (h > maxSize) {
        w *= maxSize / h; // –ü—Ä–æ–ø–æ—Ä—Ü–∏–æ–Ω–∞–ª—å–Ω–æ —É–º–µ–Ω—å—à–∞–µ–º —à–∏—Ä–∏–Ω—É
        h = maxSize;      // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é –≤—ã—Å–æ—Ç—É
      }

      // 9. –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ä–∞–∑–º–µ—Ä—ã –∫–∞–Ω–≤–∞—Å–∞ –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É–º–µ–Ω—å—à–µ–Ω–Ω—ã–º–∏ —Ä–∞–∑–º–µ—Ä–∞–º–∏ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
      canvas.width = w;
      canvas.height = h;

      // 10. –†–∏—Å—É–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –Ω–∞ –∫–∞–Ω–≤–∞—Å–µ —Å –Ω–æ–≤—ã–º–∏ —Ä–∞–∑–º–µ—Ä–∞–º–∏
      canvas.getContext("2d").drawImage(img, 0, 0, w, h);

      // 11. –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –Ω–∞ –∫–∞–Ω–≤–∞—Å–µ –≤ —Ñ–æ—Ä–º–∞—Ç base64 (JPEG) —Å –∫–∞—á–µ—Å—Ç–≤–æ–º 70%
      // callback() –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º - —Å—Ç—Ä–æ–∫–æ–π base64, –∫–æ—Ç–æ—Ä–∞—è –º–æ–∂–µ—Ç –±—ã—Ç—å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∞ –∫–∞–∫ URL –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
      callback(canvas.toDataURL("image/jpeg", 0.7));
    };

    // 12. –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∏—Å—Ç–æ—á–Ω–∏–∫ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤ —Ä–µ–∑—É–ª—å—Ç–∞—Ç —á—Ç–µ–Ω–∏—è —Ñ–∞–π–ª–∞
    img.src = event.target.result;
  };

  // 13. –ù–∞—á–∏–Ω–∞–µ–º —á—Ç–µ–Ω–∏–µ —Ñ–∞–π–ª–∞ –∫–∞–∫ Data URL (–¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ–º)
  reader.readAsDataURL(file);
}

