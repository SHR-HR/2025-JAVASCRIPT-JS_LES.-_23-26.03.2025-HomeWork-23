// –§—É–Ω–∫—Ü–∏—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è —Ç–µ–∫—É—â–µ–≥–æ –ø—Ä–æ–≥—Ä–µ—Å—Å–∞ –∏–≥—Ä—ã –≤ localStorage
function saveProgress() {
  // –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ç–µ–∫—É—â–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å–µ –∏–≥—Ä—ã –≤ –æ–±—ä–µ–∫—Ç–µ –∏ –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –≤ localStorage
  localStorage.setItem("snake_progress", JSON.stringify({
    score,             // —Ç–µ–∫—É—â–∏–π —Å—á—ë—Ç
    level,             // —Ç–µ–∫—É—â–∏–π —É—Ä–æ–≤–µ–Ω—å
    secondsElapsed,    // –ø—Ä–æ—à–µ–¥—à–µ–µ –≤—Ä–µ–º—è –∏–≥—Ä—ã
    skinColor,         // –≤—ã–±—Ä–∞–Ω–Ω—ã–π —Ü–≤–µ—Ç —Å–∫–∏–Ω–∞ –∑–º–µ–π–∫–∏
    wallsEnabled       // –≤–∫–ª—é—á–µ–Ω—ã –ª–∏ —Å—Ç–µ–Ω—ã
  }));
}

