    // +size/2 ‚Äî —á—Ç–æ–±—ã —Ç–µ–∫—Å—Ç –æ–∫–∞–∑–∞–ª—Å—è —Ç–æ—á–Ω–æ –ø–æ —Ü–µ–Ω—Ç—Ä—É —è—á–µ–π–∫–∏
