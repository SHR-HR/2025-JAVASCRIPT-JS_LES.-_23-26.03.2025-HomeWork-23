  foods = generateFoods(foods); // ‚úÖ –ü–æ–ª–Ω–æ—Å—Ç—å—é –æ–±–Ω–æ–≤–ª—è–µ–º –º–∞—Å—Å–∏–≤ –µ–¥—ã

