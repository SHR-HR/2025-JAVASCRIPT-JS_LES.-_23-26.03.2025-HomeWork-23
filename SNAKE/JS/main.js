function shadeColor(c, percent) {
  // –£–±–∏—Ä–∞–µ–º —Å–∏–º–≤–æ–ª "#" –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º HEX-—Å—Ç—Ä–æ–∫—É –≤ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
  let f = parseInt(c.slice(1), 16),

      // –ï—Å–ª–∏ percent –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ ‚Äî –∑–∞—Ç–µ–º–Ω–µ–Ω–∏–µ, –∑–Ω–∞—á–∏—Ç —Ç—è–Ω–µ–º —Ü–≤–µ—Ç –∫ —á—ë—Ä–Ω–æ–º—É (0),
      // –µ—Å–ª–∏ –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω–æ–µ ‚Äî –æ—Å–≤–µ—Ç–ª–µ–Ω–∏–µ, –∑–Ω–∞—á–∏—Ç —Ç—è–Ω–µ–º –∫ –±–µ–ª–æ–º—É (255)
      t = percent < 0 ? 0 : 255,

      // –ë–µ—Ä—ë–º –º–æ–¥—É–ª—å –ø—Ä–æ—Ü–µ–Ω—Ç–æ–≤ (0.1, 0.5 –∏ —Ç.–¥.)
      p = Math.abs(percent),

      // –ò–∑–≤–ª–µ–∫–∞–µ–º –∫–æ–º–ø–æ–Ω–µ–Ω—Ç—ã R (–∫—Ä–∞—Å–Ω—ã–π), G (–∑–µ–ª—ë–Ω—ã–π), B (—Å–∏–Ω–∏–π) –∏–∑ —Ü–≤–µ—Ç–∞
      R = f >> 16,          // –°–¥–≤–∏–≥–∞–µ–º –≤–ø—Ä–∞–≤–æ –Ω–∞ 16 –±–∏—Ç ‚Äî –ø–æ–ª—É—á–∞–µ–º –∫—Ä–∞—Å–Ω—ã–π
      G = f >> 8 & 0x00FF,  // –°–¥–≤–∏–≥–∞–µ–º –Ω–∞ 8 –±–∏—Ç –∏ –º–∞—Å–∫–∏—Ä—É–µ–º ‚Äî –ø–æ–ª—É—á–∞–µ–º –∑–µ–ª—ë–Ω—ã–π
      B = f & 0x0000FF;     // –ü–æ–ª—É—á–∞–µ–º —Å–∏–Ω–∏–π, –º–∞—Å–∫–æ–π

  // –°—á–∏—Ç–∞–µ–º –Ω–æ–≤—ã–π —Ü–≤–µ—Ç, –ø—Ä–∏–±–ª–∏–∂–∞—è –∫–∞–∂–¥—É—é –∫–æ–º–ø–æ–Ω–µ–Ω—Ç—É –∫ —Ü–µ–ª–µ–≤–æ–º—É –∑–Ω–∞—á–µ–Ω–∏—é t (0 –∏–ª–∏ 255)
  // –ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ —Ö–æ—Ç–∏–º –∑–∞—Ç–µ–º–Ω–∏—Ç—å –Ω–∞ 30%, –º—ã –±–µ—Ä—ë–º 70% –æ—Ç –∏—Å—Ö–æ–¥–Ω–æ–≥–æ –∏ 30% –æ—Ç —á—ë—Ä–Ω–æ–≥–æ
  return "#" + (
    0x1000000 +                                 // –ë–∞–∑–æ–≤—ã–π –±–∏—Ç–æ–≤—ã–π —Å–¥–≤–∏–≥ –¥–ª—è HEX
    (Math.round((t - R) * p) + R) * 0x10000 +   // –ù–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ R
    (Math.round((t - G) * p) + G) * 0x100 +     // –ù–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ G
    (Math.round((t - B) * p) + B)               // –ù–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ B
  ).toString(16).slice(1);                     // –ü–µ—Ä–µ–≤–æ–¥–∏–º –≤—Å—ë –æ–±—Ä–∞—Ç–Ω–æ –≤ HEX –∏ —É–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ —Å–∏–º–≤–æ–ª—ã
}

